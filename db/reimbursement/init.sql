-- ============================================================================
-- ers_reimbursement: the reimbursement service's OWN database.
-- Owns the domain: requests, the approval chain, reimbursements, locations.
-- users/roles here are a REPLICATED REFERENCE COPY (owner: auth-service, see
-- db/auth/init.sql) - and the copy has NO password column: the credential is
-- structurally absent from this service's world, not merely unmapped.
-- In production the copy would be kept in sync by events from auth-service;
-- with this static seed the copy is simply seeded identically (minus the
-- password) - the eventual-consistency trade is documented in the README.
-- Same schema name as the legacy monolith so the entity mappings stay put.
-- ============================================================================
CREATE SCHEMA "ExpenseReimbursementManagementSystem";
SET search_path TO "ExpenseReimbursementManagementSystem";

-- ---- replicated reference data (owner: auth-service) -----------------------

CREATE TABLE roles (
    roleid integer PRIMARY KEY,
    roles  character varying NOT NULL UNIQUE
);

CREATE TABLE users (
    userid        integer PRIMARY KEY,
    loginusername character varying NOT NULL UNIQUE,
    firstname     character varying NOT NULL,
    lastname      character varying NOT NULL,
    email         character varying NOT NULL UNIQUE,
    roles         integer REFERENCES roles (roleid)
);

-- ---- owned domain tables ----------------------------------------------------

CREATE TABLE city_state_postal (
    postalcode integer PRIMARY KEY,
    city       character varying NOT NULL,
    state      character varying NOT NULL
);

CREATE TABLE event_location (
    locationid    integer NOT NULL UNIQUE,
    street_number integer NOT NULL,
    street_name   character varying NOT NULL,
    postalcode    integer NOT NULL REFERENCES city_state_postal (postalcode),
    PRIMARY KEY (street_number, street_name, postalcode)
);

CREATE TABLE request_status (
    statusid integer PRIMARY KEY,
    status   character varying NOT NULL UNIQUE
);

CREATE TABLE request (
    requestid       integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    amount          double precision,
    eventdate       date NOT NULL,
    eventlocation   integer REFERENCES event_location (locationid),
    requestedevent  character varying NOT NULL,
    requesteruserid integer REFERENCES users (userid),
    statusid        integer REFERENCES request_status (statusid),
    CONSTRAINT request_amount_check CHECK ((amount >= (0)::double precision)),
    CONSTRAINT request_eventdate_check CHECK ((eventdate <= CURRENT_DATE))
);

CREATE TABLE employee_supervisor_jt (
    hierarchyid      integer GENERATED BY DEFAULT AS IDENTITY UNIQUE,
    useridsupervisor integer NOT NULL REFERENCES users (userid),
    useridemployee   integer NOT NULL REFERENCES users (userid),
    PRIMARY KEY (useridsupervisor, useridemployee)
);

CREATE TABLE supervisor_approval_status (
    statusid integer PRIMARY KEY,
    status   character varying NOT NULL UNIQUE
);

CREATE TABLE supervisor_approval (
    approvalid           integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dateofpreviousupdate date NOT NULL,
    requestid            integer REFERENCES request (requestid),
    hierarchyid          integer REFERENCES employee_supervisor_jt (hierarchyid),
    statusid             integer REFERENCES supervisor_approval_status (statusid),
    approval             boolean,
    CONSTRAINT supervisor_approval_dateofpreviousupdate_check CHECK ((dateofpreviousupdate <= CURRENT_DATE))
);

CREATE TABLE reimbursement_status (
    statusid integer PRIMARY KEY,
    status   character varying NOT NULL UNIQUE
);

CREATE TABLE reimbursement (
    reimbursementid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    amount          double precision,
    dateawarded     date,
    finalapprovalid integer REFERENCES supervisor_approval (approvalid),
    statusid        integer REFERENCES reimbursement_status (statusid),
    CONSTRAINT reimbursement_amount_check CHECK ((amount >= (0)::double precision)),
    CONSTRAINT reimbursement_dateawarded_check CHECK ((dateawarded <= CURRENT_DATE))
);

-- ---- messaging infrastructure ------------------------------------------------

-- Idempotency ledger for the Kafka intake: one row per correlation id ever
-- processed (successfully OR deterministically rejected). Kafka delivers
-- at-least-once - a crash between the DB commit and the offset commit replays
-- the event - so the consumer records the id in the SAME transaction as the
-- domain write and skips ids it has already seen. The PK is the guarantee.
CREATE TABLE processed_event (
    correlationid character varying PRIMARY KEY,
    processedat   timestamp with time zone NOT NULL DEFAULT now()
);

